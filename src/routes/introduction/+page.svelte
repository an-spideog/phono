<h1>Cartlann Fuaime an Bhrainse Logainmneacha</h1>
<p>
    Cartlann Fuaime de thaifeadtaí logainmneacha de chuid an Bhrainse Logainmneacha
    is ea an Chartlann seo. Cuimsítear inti breis is 1,200 uair an chloig de thaifeadtaí
    a rinneadh sna 1960idí agus sna 1970idí go príomha. Rinneadh na taifeadtaí i 24
    contae timpeall na tíre agus bailíodh logainmneacha ó bhreis is 4,000 duine le linn
    na tréimhse sin ar fad.
</p>
<p>
    Rinneadh digitiú ar an ábhar fuaime agus ar an gcatalóg a théann leis le linn 2009.
    Cruthaíodh bunachar sonraí in Fiontar mar chuid de Chomhaltacht Taighde MA a bhí
    ar siúl ag Cáit Nic Fhionnlaoich, 2010–2011, faoi scáth na Roinne Gnóthaí Pobail, 
    Comhionannais agus Gaeltachta. Mar thoradh ar an obair sin tá an t-ábhar fuaime agus 
    an chatalóg ar fáil anois ar líne d’fhoireann an Bhrainse Logainmneacha agus do 
    thaighdeoirí eile.
</p>
<p>
    <a href="http://doras.dcu.ie/16839/1/Thesis_C%C3%A1it_Nic_Fhionnlaoich.pdf" target="_top">Tráchtas Cháit Nic Fhionnlaoich »</a>
</p>
<p>
    Cuirimid ar fáil anseo bunachar sonraí teoranta den chatalóg dhigitithe, gan na
    comhaid fuaime, ionas gur féidir le daoine tuiscint a fháil ar an méid atá sna taifeadtaí
    fuaime. Ní féidir, mar gheall ar chúrsaí príobháideachais agus cead craolta, na
    taifeadtaí a chur ar fáil go poiblí ar an idirlíon.
</p>
<p>
    Má tá spéis agat in aon cheann de na taifeadtaí, scríobh ríomhphost chuig <a href="mailto:logainm@dcu.ie">
        logainm@dcu.ie</a> ag lua uimhir thagartha na dtaifeadtaí atá uait (1a1, mar
    shampla) agus ag míniú an fáth a bhfuil rochtain ar na taifeadtaí uait. Is faoin
    mBrainse Logainmneacha atá sé cinneadh a dhéanamh maidir leis na sonraí seo a chur
    ar fáil.
</p>
