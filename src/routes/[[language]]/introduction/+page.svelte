<h1>
                Sound Archive of the Placenames Branch</h1>
            <p>
                This sound archive consists of the placenames recordings of the Placenames Branch.
                It contains more than 1,200 hours of recordings that were made mainly in the 1960s
                and 1970s. The recordings were made in 24 counties around the country and placenames
                were collected from more than 4,000 people during that period.
            </p>
            <p>
                The audio material and its catalogue were digitised in 2009. A database was created
                in Fiontar as part of an MA Research Fellowship undertaken by Cáit Nic Fhionnlaoich,
                2010–2011, sponsored by the Department of Community, Equality and Gaeltacht Affairs. As a
                result of that work, the audio and catalogue material are now available online for
                the staff of the Placenames Branch and for other researchers.
            </p>
            <p>
                <a href="http://doras.dcu.ie/16839/1/Thesis_C%C3%A1it_Nic_Fhionnlaoich.pdf" target="_top">Cáit Nic Fhionnlaoich's thesis »</a>
            </p>
            <p>
                A limited database of the digitised catalogue, excluding the audio files, is available
                here to the public in order to give people some idea of the archive’s extent. The
                recordings, however, cannot be made available publicly online due to issues of privacy
                and broadcasting permission.
            </p>
            <p>
                If you are interested in any of the recordings, please email <a href="mailto:logainm@dcu.ie">
                    logainm@dcu.ie</a> with the reference number of the recording to which you would
                like access (for example, 1a1) along with the reason why. The decision to grant
                access to this data rests with the Placenames Branch.
            </p>
            <h1>
                User Guide to Sound Archives</h1>
            <ol class="guideList">
                <li>The second tab at the top of the page, <b>REELS</b>, will take you to a list of
                    all the reels in the Sound Archive.
                    <ol class="guideSubList">
                        <li>You can browse through the recordings using the navigation bar under the search
                            boxes.</li>
                        <li>You can refine your search to a particular county by typing the name of the county
                            in the <b>Text</b> search in the format Co. + county name, e.g. Co. Phort Láirge,
                            Co. Dhún na nGall, Co. Thiobraid Árann.</li>
                        <li>You can search by collector by clicking <b>select</b> in the collector search box.</li>
                    </ol>
                </li>
                <li>If you are interested in a particular reel, click on <b>tracks</b> below the reel’s
                    information. You will be taken to the tracks related to that reel. Here you will
                    see information about the recording such as the <b>place</b> associated with the
                    recording as well as the <b>recording date</b> and the <b>speaker</b>.
                    <ol class="guideSubList">
                        <li>If you click on the placename, the entry for that placename on <a href="http://www.logainm.ie/">
                            www.logainm.ie</a> will open in a new tab.</li>
                        <li>If you click on the speaker’s name you will be taken to the entry on that person
                            in the <b>SPEAKERS</b> tab, where there is more information on the speaker as well
                            as links to the other tracks related to him/her.</li>
                    </ol>
                </li>
                <li>The following searches are available under the <b>TRACKS</b> tab: <b>Text</b>, <b>
                    Speaker</b>, <b>Collector</b>, <b>Spool</b>, <b>Place</b> (the unique number of
                    the place on <a href="http://www.logainm.ie/">www.logainm.ie</a> must be used here),
                    <b>ID</b> (the number on the top left hand side of the track: 28, for example) and 
                    <b>Nickname</b> (the number of the track itself: 1a1, for example).</li>
                <li>You can search the <b>SPEAKERS</b> and the <b>COLLECTORS</b> by using the <b>Text</b>
                    search under those tabs.</li>
            </ol>
